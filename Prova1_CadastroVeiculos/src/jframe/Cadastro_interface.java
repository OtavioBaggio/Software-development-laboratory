    private void btn_inserirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_inserirActionPerformed
        Veiculo v = new Veiculo();
        v.setMarca(txt_marca.getText());
        v.setModelo(txt_modelo.getText());
        v.setAno(Integer.parseInt(txt_ano.getText()));
        v.setPlaca(txt_placa.getText());
        if(rd_preto.isSelected()){
            v.setCor("Preto");
        }else if(rd_branco.isSelected()){
            v.setCor("Branco");
        }else if(rd_prata.isSelected()){
            v.setCor("Prata");
        }else if(rd_vermelho.isSelected()){
            v.setCor("vermelho");
        }else{
            v.setCor("Azul");
        }
        
        VeiculoDAO vDAO = new VeiculoDAO();
        vDAO.inserir(v);
        
        listaVeiculos.add(v);
        arquivo.gravaArquivo(v);
        salvarNoBanco(v);

        JOptionPane.showMessageDialog(null, "Ve√≠culo adicionado com sucesso");

        DefaultTableModel tabela = (DefaultTableModel) tbl_veiculo.getModel();
        tabela.addRow(v.obterDados());

        limparFormulario();
        
        
    }//GEN-LAST:event_btn_inserirActionPerformed


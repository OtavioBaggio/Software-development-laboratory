    private void salvarNoBanco(Veiculo v) {
        String url = "jdbc:mysql://localhost:3306/BDveiculo";
        String usuario = "root";
        String senha = "12345678";

        String sql = "INSERT INTO Veiculos (marca, modelo, ano, placa, cor) VALUES (?, ?, ?, ?, ?)";

        try (Connection conn = DriverManager.getConnection(url, usuario, senha);
             PreparedStatement stmt = conn.prepareStatement(sql)) {

            stmt.setString(1, v.getMarca());
            stmt.setString(2, v.getModelo());
            stmt.setInt(3, v.getAno());
            stmt.setString(4, v.getPlaca());
            stmt.setString(5, v.getCor());

            stmt.executeUpdate();

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Erro ao salvar no banco: " + e.getMessage());
        }
}

    
    

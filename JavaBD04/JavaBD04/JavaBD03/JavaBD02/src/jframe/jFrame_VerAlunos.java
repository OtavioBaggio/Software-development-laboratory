    private void btn_consultaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_consultaActionPerformed
        int idPessoa = Integer.parseInt(txt_idPessoa.getText());
        PessoaDAO pDAO = new PessoaDAO();
        
        Pessoa p = pDAO.getPessoaPorId(idPessoa);
        if(p == null){
            limparFormulario();
            JOptionPane.showMessageDialog(this, "Pessoa n√£o encontrada!");
        }
        else{
            txt_idPessoa.setText(String.valueOf(p.getId()));
            txt_nome.setText(p.getNome());
            //txt_sexo.setText(p.getSexo());
            if(p.getSexo().equals("M")){
                rd_masculino.setSelected(true);
            }
            else{
                rd_feminino.setSelected(true);
            }
            //txt_Idioma.setText(p.getIdioma());
            cmb_idioma.setSelectedItem(p.getIdioma());
            
        }
    }//GEN-LAST:event_btn_consultaActionPerformed

    private void btn_atualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_atualizarActionPerformed
        String sexo = null;
        if(rd_masculino.isSelected()){
            sexo = "M";
        }
        else if(rd_feminino.isSelected()){
            sexo = "F";
        }
        Pessoa p = new Pessoa();
        p.setId(Integer.parseInt(txt_idPessoa.getText()));
        p.setNome(txt_nome.getText());
        p.setSexo(sexo);
        p.setIdioma(cmb_idioma.getSelectedItem().toString());
        
        PessoaDAO pDAO = new PessoaDAO();
        pDAO.editar(p);
        limparFormulario();
        
    }//GEN-LAST:event_btn_atualizarActionPerformed

    private void btn_excluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_excluirActionPerformed
        int resposta = JOptionPane.showConfirmDialog(null, "Deseja realmente excluir?", "Exclus√£o", JOptionPane.YES_NO_OPTION);
        
        if(resposta == JOptionPane.YES_OPTION){
            PessoaDAO pDAO = new PessoaDAO();
            pDAO.excluir(Integer.parseInt(txt_idPessoa.getText()));
            limparFormulario();
        }
    }//GEN-LAST:event_btn_excluirActionPerformed


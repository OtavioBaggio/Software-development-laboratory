    private void btn_salvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_salvarActionPerformed
        String nome = txt_nome.getText();
        int idade = Integer.parseInt(txt_idade.getText());
        String disciplina = txt_disciplina.getText();
        
        professores p = new professores();
        p.setNome(nome);
        p.setIdade(idade);
        p.setDisciplina(disciplina);
        
        ProfessoresDAO pDAO = new ProfessoresDAO();
        pDAO.inserir(p);
        
        JOptionPane.showInternalMessageDialog(null, "CADASTRO REALIZADO COM SUCESSO");
        
        txt_nome.setText("");
        txt_idade.setText("");
        txt_disciplina.setText("");
    }//GEN-LAST:event_btn_salvarActionPerformed

